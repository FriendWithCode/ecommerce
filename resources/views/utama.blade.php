const CSRF_TOKEN = '{{ csrf_token() }}';
function fmtCur(n){return n.toString().replace(/\B(?=(\d{3})+(?!\d))/g,'.');}

document.addEventListener('DOMContentLoaded',function(){
    // Isotope
    const iso=new Isotope('#product-grid',{itemSelector:'.product-item',layoutMode:'fitRows'});
    document.querySelectorAll('.filter-btn').forEach(btn=>{
        btn.addEventListener('click',function(){
            document.querySelectorAll('.filter-btn').forEach(b=>b.classList.remove('active'));
            this.classList.add('active');
            iso.arrange({filter:this.getAttribute('data-filter')});
        });
    });
    // Back to top
    const btt=document.querySelector('.back-to-top');
    window.addEventListener('scroll',()=>btt.classList.toggle('active',window.scrollY>300));
    // Smooth scroll
    document.querySelectorAll('a[href^="#"]').forEach(a=>{
        a.addEventListener('click',function(e){
            const t=this.getAttribute('href');
            if(t==='#')return;
            const el=document.querySelector(t);
            if(el){e.preventDefault();window.scrollTo({top:el.offsetTop-80,behavior:'smooth'});}
        });
    });
    // Reveal
    const obs=new IntersectionObserver(entries=>{
        entries.forEach(e=>{if(e.isIntersecting)e.target.classList.add('visible');});
    },{threshold:.12});
    document.querySelectorAll('.reveal').forEach(el=>obs.observe(el));
    // Load cart on page load
    loadCart();
    // Auto-dismiss alerts
    const af=document.getElementById('alertFloat');
    if(af) setTimeout(()=>{af.style.opacity='0';af.style.transform='translateX(-50%) translateY(-20px)';setTimeout(()=>af.remove(),400);},4000);
});

// === CART FUNCTIONS ===
function toggleCart(){
    const s=document.getElementById('cartSidebar');
    const o=document.getElementById('cartOverlay');
    if(s){s.classList.toggle('open');o.classList.toggle('open');}
}

function addToCart(produkId){
    fetch('/cart/add',{
        method:'POST',
        headers:{'Content-Type':'application/json','X-CSRF-TOKEN':CSRF_TOKEN,'Accept':'application/json'},
        body:JSON.stringify({produk_id:produkId,qty:1})
    })
    .then(r=>r.json())
    .then(data=>{
        if(data.success){
            showToast(data.message,'success');
            updateBadge(data.cartCount);
            loadCart();
        } else {
            showToast(data.message||'Gagal menambahkan','error');
        }
    })
    .catch(()=>showToast('Silakan login terlebih dahulu','error'));
}

function updateCartQty(produkId,qty){
    fetch('/cart/update',{
        method:'POST',
        headers:{'Content-Type':'application/json','X-CSRF-TOKEN':CSRF_TOKEN,'Accept':'application/json'},
        body:JSON.stringify({produk_id:produkId,qty:qty})
    })
    .then(r=>r.json())
    .then(data=>{
        if(data.success){
            updateBadge(data.cartCount);
            renderCart(data.cart,data.cartTotal);
        }
    });
}

function removeCartItem(produkId){
    fetch('/cart/remove/'+produkId,{
        method:'DELETE',
        headers:{'X-CSRF-TOKEN':CSRF_TOKEN,'Accept':'application/json'}
    })
    .then(r=>r.json())
    .then(data=>{
        if(data.success){
            showToast(data.message,'success');
            updateBadge(data.cartCount);
            renderCart(data.cart,data.cartTotal);
        }
    });
}

function loadCart(){
    fetch('/cart/data',{headers:{'Accept':'application/json'}})
    .then(r=>r.json())
    .then(data=>{
        updateBadge(data.cartCount);
        renderCart(data.cart,data.cartTotal);
    })
    .catch(()=>{});
}

function renderCart(items,total){
    const ci=document.getElementById('cartItems');
    const ce=document.getElementById('cartEmpty');
    const cf=document.getElementById('cartFooter');
    const tv=document.getElementById('cartTotalValue');
    if(!ci)return;

    if(!items||items.length===0){
        ci.innerHTML='';
        if(ce)ce.style.display='block';
        if(cf)cf.style.display='none';
        return;
    }

    if(ce)ce.style.display='none';
    if(cf)cf.style.display='block';
    if(tv)tv.textContent='Rp '+fmtCur(total);

    let html='';
    items.forEach(item=>{
        const imgSrc=item.image?'/storage/'+item.image:'https://images.unsplash.com/photo-1593941707882-a5bba14938c7?auto=format&fit=crop&w=200&q=80';
        html+=`<div class="cart-item">
            <img src="${imgSrc}" class="cart-item-img" alt="${item.nama}" onerror="this.src='https://images.unsplash.com/photo-1593941707882-a5bba14938c7?auto=format&fit=crop&w=200&q=80'">
            <div class="cart-item-info">
                <div class="cart-item-name">${item.nama}</div>
                <div class="cart-item-tipe">${item.tipe||''}</div>
                <div class="cart-item-price">Rp ${fmtCur(item.subtotal)}</div>
                <div class="d-flex align-items-center justify-content-between mt-2">
                    <div class="cart-item-qty">
                        <button class="cart-qty-btn" onclick="updateCartQty(${item.id},${item.qty-1})" ${item.qty<=1?'disabled':''}><i class="fas fa-minus"></i></button>
                        <span class="cart-qty-val">${item.qty}</span>
                        <button class="cart-qty-btn" onclick="updateCartQty(${item.id},${item.qty+1})" ${item.qty>=item.stok?'disabled':''}><i class="fas fa-plus"></i></button>
                    </div>
                    <small class="text-muted" style="font-size:0.75rem">Stok: ${item.stok}</small>
                </div>
            </div>
            <button class="cart-item-remove" onclick="removeCartItem(${item.id})" title="Hapus"><i class="fas fa-trash-alt"></i></button>
        </div>`;
    });
    ci.innerHTML=html;
}

function updateBadge(count){
    const b=document.getElementById('cartBadge');
    if(!b)return;
