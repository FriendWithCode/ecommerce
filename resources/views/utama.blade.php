function adjustDirectQty(kode, change, price, max) {
    const input = document.getElementById('directQtyInput' + kode);
    let val = parseInt(input.value) + change;
    if (val < 1) val = 1;
    if (val > max) val = max;
    input.value = val;
    updateDirectTotal(kode, price, max);
}

function updateDirectTotal(kode, price, max) {
    const input = document.getElementById('directQtyInput' + kode);
    let val = parseInt(input.value) || 1;
    if (val < 1) val = 1;
    if (val > max) val = max;
    input.value = val;

    const display = document.getElementById('directQtyDisplay' + kode);
    const totalDisp = document.getElementById('directTotalDisplay' + kode);
    const formQty = document.getElementById('directFormQty' + kode);

    if (display) display.textContent = val;
    if (totalDisp) totalDisp.textContent = 'Rp ' + fmtCur(val * price);
    if (formQty) formQty.value = val;
}
